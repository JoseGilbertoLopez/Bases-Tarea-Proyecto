CREATE TABLE INTENDENCIA_INFO_PERSONAL(
ID_Intendencia Number GENERATED ALWAYS AS IDENTITY (START with 1 INCREMENT by 1),
No_Seguro_Social VARCHAR(255) NOT NULL,
RFC VARCHAR(255) NOT NULL,
Nombre VARCHAR(255) NOT NULL,
Apellido_Paterno VARCHAR(255) NOT NULL,
Apellido_Materno VARCHAR(255) NOT NULL
);

ALTER TABLE INTENDENCIA_INFO_PERSONAL
ADD constraint PK_INTENDENCIA_PERSONAL
Primary Key(ID_Intendencia,No_Seguro_Social,RFC);

CREATE TABLE INTENDENCIA_INFO_CONTACTO(
RFC VARCHAR(255) NOT NULL,
Nombre VARCHAR(255) NOT NULL,
Apellido_Materno VARCHAR(255) NOT NULL,
Apellido_Paterno VARCHAR(255) NOT NULL,
Direccion VARCHAR(255) NOT NULL,
Correo_Electronico VARCHAR(255) NOT NULL,
Telefono VARCHAR(255) NOT NULL
);

ALTER TABLE INTENDENCIA_INFO_CONTACTO
ADD constraint PK_Intendencia_Contacto
Primary Key(RFC,Nombre);

ALTER TABLE INTENDENCIA_INFO_PERSONAL
ADD constraint FK1_INTENDENCIA_PERSONAL
FOREIGN KEY (RFC,Nombre)
REFERENCES INTENDENCIA_INFO_CONTACTO(RFC,Nombre)
ON DELETE CASCADE;

